package com.app.partyplanets.Data;

public class RatingDatum {
    public double cleanliness;
    public double staff_and_services;
    public double amenities;
    public double eco_friendliness;
    public double facilities;


    public double getCleanliness() {
        return cleanliness;
    }

    public void setCleanliness(double cleanliness) {
        this.cleanliness = cleanliness;
    }

    public double getStaff_and_services() {
        return staff_and_services;
    }

    public void setStaff_and_services(double staff_and_services) {
        this.staff_and_services = staff_and_services;
    }

    public double getAmenities() {
        return amenities;
    }

    public void setAmenities(double amenities) {
        this.amenities = amenities;
    }

    public double getEco_friendliness() {
        return eco_friendliness;
    }

    public void setEco_friendliness(double eco_friendliness) {
        this.eco_friendliness = eco_friendliness;
    }

    public double getFacilities() {
        return facilities;
    }

    public void setFacilities(double facilities) {
        this.facilities = facilities;
    }
}
